//Documentando uma calculadora
/**
 * Calculadora simples para operações matemáticas.
 * @author Fernando Souza
 * @since 1.0
 */
public class Calculadora {
    /**
     * REALIZA A DIVISÃO DE DOIS NÚMEROS INTEIROS.
     * @param dividendo
     * @param divisor
     * @return
     */
    Integer dividir(Integer dividendo, Integer divisor){
        return dividendo / divisor;
    }
}
